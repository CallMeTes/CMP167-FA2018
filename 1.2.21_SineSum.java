public class SineSum
{
  public static void main(String ar[])
    {
    
      //User enters the value of t
    
      double t=Double.parseDouble(ar[0]);
    
      //Calculate the formula: sin(2t)+sin(3t)
    
      double result=Math.sin(2*t)+Math.sin(3*t);
    
      //Show the result of the program
      
      System.out.println(result);
      
    }//End Main
    
}//End Class
